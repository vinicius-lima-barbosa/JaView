import mongoose, { Schema } from "mongoose";

const reviewSchemaUsers = new Schema({
  movie_id: {
    type: String,
    required: true,
  },
  review: {
    type: String,
    required: true,
  },
  rating: {
    type: Number,
    min: 0,
    max: 5,
    required: true,
  },
  created_at: {
    type: Date,
    default: Date.now,
  },
});

const userSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["user", "admin"],
    default: "user",
  },
  reviews: [reviewSchemaUsers],
});

export const User = mongoose.model("User", userSchema);
